class RelationalOperatorDemo{

public static void main(String args[])
{
int p = 1000;
int q = 2000;
System.out.println(p==q); //false
System.out.println(p!=q); //true
System.out.println(p<q); //true
System.out.println(p<=q); //true
System.out.println(p>q); //false
System.out.println(p>=q); //false

}
}
